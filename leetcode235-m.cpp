    class Solution {
    public:
        TreeNode* ret = nullptr;
        TreeNode* lowestCommonAncestor(TreeNode* root, TreeNode* p, TreeNode* q) {
            Di(root, p, q);
            return ret;
        }


        void Di(TreeNode* root, TreeNode* p, TreeNode* q) {
            if (root) {
                if (root->val > p->val && root->val < q->val ||
                    (root->val<p->val && root->val>q->val) ||
                    (root->val == p->val || root->val == q->val)) {
                    ret = root;
                    return;
                }//可以用else 啊，无语
                if (root->val > p->val && root->val > q->val) {
                    Di(root->left, p, q);
                }
                if (root->val < p->val && root->val < q->val) {
                    Di(root->right, p, q);
                }


            }
        }
    };


